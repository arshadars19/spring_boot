public class pattern_1 {
    public static void main(String[] args) {
        int rows = 3;
        int col = 3;

        for(int i =0;i<=rows;i++)
        {
            for(int j = 0;j<=col;j++)
            {
                System.out.print("*");
            }
            System.out.println("");
        }
    }
}
